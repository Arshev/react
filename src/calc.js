class Calc {
    summ(firstNumber, secondNumber) {
       return firstNumber + secondNumber
    }
    substr(firstNumber, secondNumber) {
        return firstNumber - secondNumber
    }
    mult(firstNumber, secondNumber) {
        return firstNumber * secondNumber
    }
    division(firstNumber, secondNumber) {
        return firstNumber / secondNumber
    }
}

export default Calc;
